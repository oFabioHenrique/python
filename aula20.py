primeiro_valor = input('Digite um valor: ')

segundo_valor = input('Digite um valor: ')

if primeiro_valor > segundo_valor:
    print(f'o primerio valor: {primeiro_valor} é maior que o segundo valor: {segundo_valor}')

elif primeiro_valor == segundo_valor:
    print(f'o primerio valor: {primeiro_valor} é igual ao segundo valor: {segundo_valor}')
else:
   print(f'o segundo valor: {segundo_valor} é maior que o primerio valor: {primeiro_valor}')  
