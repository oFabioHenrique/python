'''
Operadores de atribuição
=  +=  -=  *=  /=  //=  **=  %=
'''
contador = 0

contador += 1
print(contador)

contador -= 1
print(contador)

contador *= 1
print(contador)

contador /= 1
print(contador)

contador //= 1
print(contador)

contador **= 1
print(contador)

contador %= 1
print(contador)
